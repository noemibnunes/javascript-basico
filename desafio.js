//Construa funções para somar, subtrair, dividir e multiplicar, mas cada uma delas só pode ocupar uma única linha.

const somar = (x,y) => x+y;
const subtrair = (x,y) => x-y;
const dividir = (x,y) => x/y;
const multiplicar = (x,y) => x*y;

console.log("Resultado da soma: " + somar(2,2));
console.log("Resultado da subtração: " + subtrair(2,2));
console.log("Resultado da divisão: " + dividir(2,2));
console.log("Resultado da multiplicação: " + multiplicar(2,2));